export const dictionaries = { en, "en-GB": en, fr, es, yo, ha, ig, pcm, sw, am, zu };

let activeLocale = DEFAULT_LOCALE;

export function resolveLocale(locale) {
  if (!locale) return DEFAULT_LOCALE;
  if (SUPPORTED_LOCALES.includes(locale)) return locale;
  const baseLocale = `${locale}`.split("-")[0];
  return SUPPORTED_LOCALES.includes(baseLocale) ? baseLocale : DEFAULT_LOCALE;
}

export function setActiveLocale(locale) {
  activeLocale = resolveLocale(locale);
}

export function getActiveLocale() {
  return activeLocale;
}

function readTranslation(locale, key) {
  return `${key}`.split(".").reduce((value, part) => value?.[part], dictionaries[resolveLocale(locale)] || dictionaries[DEFAULT_LOCALE]);
}

export function translate(locale, key, params = {}) {
  const template = readTranslation(locale, key) ?? readTranslation(DEFAULT_LOCALE, key) ?? key;
  if (typeof template !== "string") return key;
  return template.replace(/\{(\w+)\}/g, (_, token) => `${params[token] ?? `{${token}}`}`);
}

export function createTranslator(locale) {
  return (key, params) => translate(locale, key, params);
}